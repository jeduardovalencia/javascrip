let numero_1  = parseInt (prompt("ingrese primer numero   :"))
let numero_2  = parseInt (prompt("ingrese segundo numero   :")) 
let modulo = numero_1 % numero_2
let sum = numero_1 + numero_2
let resta = numero_1 - numero_2
let cociente = numero_1 / numero_2
alert(`su suma es ${sum} su resta ${resta} su modulo ${modulo} su cociente ${cociente} `)

